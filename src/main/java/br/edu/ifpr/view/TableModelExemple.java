    private void btnRegistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrerActionPerformed
        // TODO add your handling code here:
        String name = tfName.getText();
        int age = Integer.valueOf(tfAge.getText());
        String cpf = tfCPF.getText();
        String address = tfAddress.getText();

        Person p = new Person(name, age, cpf, address);
        personTableModel.add(p);
    }//GEN-LAST:event_btnRegistrerActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int row = tblPerson.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada!");
        } else {
            personTableModel.remove(row);
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnShowDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowDataActionPerformed
        // TODO add your handling code here:
        int row = tblPerson.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Nenhuma linha selecionada!");
        } else {
            Person p = personTableModel.get(row);
            JOptionPane.showMessageDialog(null, p.toString());
            //personTableModel.remove(row);
        }
    }//GEN-LAST:event_btnShowDataActionPerformed

